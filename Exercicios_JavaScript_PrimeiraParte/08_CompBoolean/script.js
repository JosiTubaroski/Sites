
// Comparações

// >        - MAIOR QUE
// <        - MENOR QUE
// >=       - MAIOR OU IGUAL
// <=       - MENOR OU IGUAL
// ==       - IGUAL
// !=       - DIFERENTE
// ===      - IDENTICO

console.log(10 > 5); // TRUE
console.log(50 < 100); // TRUE
console.log(100 >= 100); // TRUE
console.log(100 <= 50); // FALSE
console.log(50 == 50); // TRUE
console.log('vinicius' != 'vinicius'); // false
console.log('5' === 5); // false
console.log(5 === 5); // true